<template>
  <div>
    <canvas
      id="canvasIntertwine"
      style="width: 800px; height: 600px; border:1px solid #e1e1e1;"
      @contextmenu.prevent
    ></canvas>
  </div>
</template>
<script>
function randomIntFromInterval(mn, mx) {
  return ~~(Math.random() * (mx - mn + 1) + mn);
}

export default {
  data() {
    return {
      cw: 800,
      ch: 600,
      cx: 0,
      cy: 0,
      requestId: null,
      circlesRy: [],
      circlesNum: 15
    };
  },
  mounted() {
    var canvas = document.getElementById("canvasIntertwine");
    var ctx = canvas.getContext("2d");

    this.cx = this.cw / 2;
    this.cy = this.ch / 2;

    ctx.fillStyle = "#000";

    for (var i = 0; i < this.circlesNum; i++) {
      var circle = new this.Circle();
      this.circlesRy.push(circle);
    }
    setTimeout(() => {
      this.init();

      window.addEventListener("resize", this.init, false);
    }, 15);
  },
  methods: {
    init() {
      this.circlesRy.length = 0;
      for (var i = 0; i < this.circlesNum; i++) {
        var circle = new this.Circle();
        this.circlesRy.push(circle);
      }

      if (this.requestId) {
        window.cancelAnimationFrame(this.requestId);
        this.requestId = null;
      }

      this.draw();
    },
    draw() {
      var canvas = document.getElementById("canvasIntertwine");
      var ctx = canvas.getContext("2d");
      this.requestId = window.requestAnimationFrame(this.draw);
      ctx.clearRect(0, 0, this.cw, this.ch);
      for (var i = 0; i < this.circlesRy.length; i++) {
        var c = this.circlesRy[i];
        c.update();
        c.draw();
      }
      for (var i = 0; i < this.circlesRy.length; i++) {
        var c = this.circlesRy[i];
        for (var j = i + 1; j < this.circlesRy.length; j++) {
          var c1 = this.circlesRy[j];
          var d = this.dist(c, c1);
          if (d < c.r + c1.r && d > Math.abs(c.r - c1.r)) {
            this.getIntersection(c, c1, d);
          }
        }
      }
    },
    Circle() {
      this.r = randomIntFromInterval(25, 170);
      this.x = randomIntFromInterval(this.r, this.cw - this.r);
      this.y = randomIntFromInterval(this.r, this.ch - this.r);

      this.vx = randomIntFromInterval(25, 100) / 100;
      this.vy = randomIntFromInterval(25, 100) / 100;

      this.update = function() {
        this.edges();
        this.x += this.vx;
        this.y += this.vy;
      };

      this.edges = function() {
        if (this.x < this.r || this.x > this.cw - this.r) {
          this.vx *= -1;
        }
        if (this.y < this.r || this.y > this.ch - this.r) {
          this.vy *= -1;
        }
      };

      this.draw = () => {
        var canvas = document.getElementById("canvasIntertwine");
        var ctx = canvas.getContext("2d");
        ctx.strokeStyle = "#ccc";
        ctx.beginPath();
        ctx.arc(this.x, this.y, this.r, 0, 2 * Math.PI);
        ctx.stroke();
      };
    },
    getIntersection(p1, p2, d) {
      var p3 = {}; // middle point
      var p4 = {}; // intersection 1
      var p5 = {}; // intersection 2

      var a =
        (Math.pow(p1.r, 2) - Math.pow(p2.r, 2) + Math.pow(d, 2)) / (2 * d);
      var h = Math.sqrt(Math.pow(p1.r, 2) - Math.pow(a, 2));

      p3.x = p1.x + (a * (p2.x - p1.x)) / d;
      p3.y = p1.y + (a * (p2.y - p1.y)) / d;

      p4.x = p3.x + (h * (p2.y - p1.y)) / d;
      p4.y = p3.y - (h * (p2.x - p1.x)) / d;

      p5.x = p3.x - (h * (p2.y - p1.y)) / d;
      p5.y = p3.y + (h * (p2.x - p1.x)) / d;

      this.markPoint(p4);
      this.markPoint(p5);
    },

    dist(p1, p2) {
      var dx = p2.x - p1.x;
      var dy = p2.y - p1.y;
      return Math.sqrt(dx * dx + dy * dy);
    },

    markPoint(p) {
      var canvas = document.getElementById("canvasIntertwine");
      var ctx = canvas.getContext("2d");
      ctx.fillStyle = "#000";
      ctx.beginPath();
      ctx.arc(p.x, p.y, 2, 0, 2 * Math.PI);
      ctx.fill();
    }
  }
};
</script>
